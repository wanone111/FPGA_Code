#include <hls_stream.h>

extern "C" {

void mm2s25(float* mem, hls::stream<float>& s, int size) {
    for (unsigned i = 0; i < size; ++i) {
#pragma HLS PIPELINE II = 1
        s.write(mem[i]);
    }
}
}
